'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import {
  SidebarMenu,
  SidebarMenuItem,
  SidebarMenuButton,
} from '@/components/ui/sidebar';
import { Search, Bus, Star, AlertCircle } from 'lucide-react';

export const navItems = [
  { href: '/', label: 'Route Finder', icon: Search },
  { href: '/routes', label: 'All Routes', icon: Bus },
  { href: '/saved', label: 'Saved Routes', icon: Star },
  { href: '/report', label: 'Report an Issue', icon: AlertCircle },
];

export function Nav() {
  const pathname = usePathname();

  return (
    <SidebarMenu>
      {navItems.map((item) => (
        <SidebarMenuItem key={item.href}>
          <SidebarMenuButton
            asChild
            isActive={pathname === item.href}
            tooltip={item.label}
          >
            <Link href={item.href}>
              <item.icon className="h-5 w-5" />
              <span>{item.label}</span>
            </Link>
          </SidebarMenuButton>
        </SidebarMenuItem>
      ))}
    </SidebarMenu>
  );
}
